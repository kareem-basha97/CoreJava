package assignedtask;

public class UnaryOperators {

	public static void main(String[] args)
	{
	
       int a=4, c=3;
		
		int b = a++;
		
		int d = ++c;
		
		int e = c--;
		
		int f = --a;
		
		System.out.println(b);
		
		System.out.println(d);
		
		System.out.println(e);
		
		System.out.println(f);
		
		

	}

}
