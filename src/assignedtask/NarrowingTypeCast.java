package assignedtask;

public class NarrowingTypeCast {

	public static void main(String[] args) 
	{
	
		float a = 4.5f;   
		
		int c = (int)a; 
		
		System.out.println("Before type casting float value is "+a);  
		  
		System.out.println("After type casting int value is "+c);
	}

}
